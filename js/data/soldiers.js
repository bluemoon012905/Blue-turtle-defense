const armorCost = {
  light: { gold: 10 },
  medium: { gold: 18 },
  heavy: { gold: 28 },
};

const armorTime = {
  light: { trainTime: 0.5 },
  medium: { trainTime: 1 },
  heavy: { trainTime: 1.5 },
};

function createArmor(id, name, meleeArmor, pierceArmor, tier) {
  return {
    id,
    name,
    type: 'armor',
    branch: 'infantry',
    armorClass: id.split('_').slice(0, 2).join('_'),
    weight: tier,
    statMods: { meleeArmor, pierceArmor },
    costMods: { ...armorCost[tier] },
    timeMods: { ...armorTime[tier] },
    description: `${meleeArmor}/${pierceArmor} armor`,
  };
}

